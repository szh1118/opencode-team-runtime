async function main() {
  const opts = parseArgs(process.argv.slice(2));
  const project = path.resolve(opts.project);
  ensure(project);
  if (opts.command === "help" || opts.command === "--help" || opts.command === "-h") return help();
  if (opts.command === "doctor") {
    const s = status(project);
    console.log(`P6 memory OK: ${s.files.events}`);
    console.log(`Mode: ${s.mode}; events: ${s.events}; suggestions: ${s.suggestions}`);
    return;
  }
  if (opts.command === "status") return print(status(project), opts.json);
  if (opts.command === "record") return print(record(project, opts), opts.json);
  if (opts.command === "learn") return print(learn(project, opts), opts.json);
  if (opts.command === "analyze") return print(analyze(project, opts), opts.json);
  if (opts.command === "suggestions") return print(generateSuggestions(project, opts), opts.json);
  if (opts.command === "pack") return print(exportPack(project, opts.query, opts), opts.json);
  if (opts.command === "approve") return print(approve(project, opts.id, "approved", opts.note), opts.json);
  if (opts.command === "reject") return print(approve(project, opts.id, "rejected", opts.note), opts.json);
  throw new Error(`Unknown command: ${opts.command}`);
}

if (import.meta.url === `file://${process.argv[1]}`) {
  main().catch(err => { console.error(`memory-runner error: ${err.message}`); process.exit(1); });
}
